  /**
   * 查找与给定名称最匹配的地理位置坐标。
   * @param {string} name 要查找的地点名称.
   * @returns {[number, number]|null} 返回一个包含[纬度, 经度]的数组，如果找不到则返回null.
   */
  const geocode = (name) => {
    if (!name) return null;
  
    // 直接匹配
    if (locations[name]) {
      return locations[name];
    }
  
    // 尝试模糊匹配 (例如, "北京" 应该匹配 "北京市")
    for (const key in locations) {
      if (name.includes(key) || key.includes(name)) {
        return locations[key];
      }
    }
  
    return null;
  };
  
